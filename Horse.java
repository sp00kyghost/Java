public class Horse
{
   private String name;
   private String color;
   private int Year;
  
public void setName(String name)
   {
   this.name = name;
   }
public void setColor(String color)
   {
   this.color = color;
   }
public void setYear(int Year)
   {
   this.Year = Year;
   }
public String getName()
{
   return name;
}
public String getColor()
{
   return color;
}
public int getYear()
{
   return Year;
}


}